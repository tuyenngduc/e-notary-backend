package com.actvn.enotary.enums;

/**
 * Common types of contracts for notary requests.
 */
public enum ContractType {
    TRANSFER_OF_PROPERTY, // hợp đồng chuyển nhượng nhà đất
    POWER_OF_ATTORNEY,    // giấy ủy quyền
    LOAN_AGREEMENT,       // hợp đồng vay mượn
    WILL,                 // di chúc
    MARRIAGE_CONTRACT,    // hợp đồng hôn nhân / thỏa thuận
    BUSINESS_CONTRACT,    // hợp đồng thương mại / dịch vụ
    OTHER
}

